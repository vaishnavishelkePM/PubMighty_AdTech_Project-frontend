'use client';

import { useState } from 'react';
import { useBoolean } from 'minimal-shared/hooks';

import Box from '@mui/material/Box';
import Link from '@mui/material/Link';
import Avatar from '@mui/material/Avatar';
import Drawer from '@mui/material/Drawer';
import MenuList from '@mui/material/MenuList';
import MenuItem from '@mui/material/MenuItem';
import Typography from '@mui/material/Typography';
import IconButton from '@mui/material/IconButton';

import { paths } from 'src/routes/paths';
import { usePathname } from 'src/routes/hooks';
import { RouterLink } from 'src/routes/components';

import { CONFIG } from 'src/global-config';
import { useAppContext } from 'src/contexts/app-context';

import { Label } from 'src/components/label';
import { Iconify } from 'src/components/iconify';
import { Scrollbar } from 'src/components/scrollbar';
import { AnimateBorder } from 'src/components/animate';

import UpdateProfileDialog from 'src/sections/dashboard/profile/update-profile';
import UpdatePasswordDialog from 'src/sections/dashboard/profile/update-password';

import { LogOutButton } from './log-out-button';
import { AccountButton } from './account-button';

export function AccountDrawer({ data = [], sx, ...other }) {
  const pathname = usePathname();
  const { user } = useAppContext();

  const { value: open, onFalse: onClose, onTrue: onOpen } = useBoolean();

  const [openPasswordDialog, setOpenPasswordDialog] = useState(false);
  const [openProfileDialog, setOpenProfileDialog] = useState(false);

  const profileImageUrl = user?.avatar ? `${CONFIG.apiUrl}/upload/admin/${user.avatar}` : undefined;

  const renderAvatar = () => (
    <AnimateBorder
      sx={{ mb: 2, p: '6px', width: 96, height: 96, borderRadius: '50%' }}
      slotProps={{
        primaryBorder: { size: 120, sx: { color: 'primary.main' } },
      }}
    >
      <Avatar src={profileImageUrl} alt={user?.username} sx={{ width: 1, height: 1 }}>
        {!profileImageUrl && user?.username?.charAt(0)?.toUpperCase()}
      </Avatar>
    </AnimateBorder>
  );

  const renderList = () => (
    <MenuList
      disablePadding
      sx={[
        (theme) => ({
          py: 3,
          px: 2.5,
          borderTop: `dashed 1px ${theme.vars.palette.divider}`,
          borderBottom: `dashed 1px ${theme.vars.palette.divider}`,
          '& li': { p: 0 },
        }),
      ]}
    >
      {data.map((option) => {
        const rootLabel = pathname.includes('/dashboard') ? 'Home' : 'Dashboard';
        const rootHref = pathname.includes('/dashboard') ? '/' : paths.dashboard.root;

        return (
          <MenuItem key={option.label}>
            <Link
              component={RouterLink}
              href={option.label === 'Home' ? rootHref : option.href}
              color="inherit"
              underline="none"
              onClick={onClose}
              sx={{
                p: 1,
                width: 1,
                display: 'flex',
                typography: 'body2',
                alignItems: 'center',
                color: 'text.secondary',
                '& svg': { width: 24, height: 24 },
                '&:hover': { color: 'text.primary' },
              }}
            >
              {option.icon}

              <Box component="span" sx={{ ml: 2 }}>
                {option.label === 'Home' ? rootLabel : option.label}
              </Box>

              {option.info && (
                <Label color="error" sx={{ ml: 1 }}>
                  {option.info}
                </Label>
              )}
            </Link>
          </MenuItem>
        );
      })}

      <MenuItem
        onClick={() => {
          onClose();
          setOpenProfileDialog(true);
        }}
      >
        <Box
          sx={{
            p: 1,
            width: 1,
            display: 'flex',
            typography: 'body2',
            alignItems: 'center',
            color: 'text.secondary',
            cursor: 'pointer',
            '& svg': { width: 24, height: 24 },
            '&:hover': { color: 'text.primary' },
          }}
        >
          <Iconify icon="solar:user-bold-duotone" />
          <Box component="span" sx={{ ml: 2 }}>
            Update Profile
          </Box>
        </Box>
      </MenuItem>

      <MenuItem
        onClick={() => {
          onClose();
          setOpenPasswordDialog(true);
        }}
      >
        <Box
          sx={{
            p: 1,
            width: 1,
            display: 'flex',
            typography: 'body2',
            alignItems: 'center',
            color: 'text.secondary',
            cursor: 'pointer',
            '& svg': { width: 24, height: 24 },
            '&:hover': { color: 'text.primary' },
          }}
        >
          <Iconify icon="solar:lock-password-bold-duotone" />
          <Box component="span" sx={{ ml: 2 }}>
            Update Password
          </Box>
        </Box>
      </MenuItem>
    </MenuList>
  );

  return (
    <>
      <AccountButton
        onClick={onOpen}
        photoURL={profileImageUrl}
        displayName={user?.username}
        sx={sx}
        {...other}
      />

      <Drawer
        open={open}
        onClose={onClose}
        anchor="right"
        slotProps={{ backdrop: { invisible: true } }}
        PaperProps={{ sx: { width: 320 } }}
      >
        <IconButton
          onClick={onClose}
          sx={{
            top: 12,
            left: 12,
            zIndex: 9,
            position: 'absolute',
          }}
        >
          <Iconify icon="mingcute:close-line" />
        </IconButton>

        <Scrollbar>
          <Box
            sx={{
              pt: 8,
              display: 'flex',
              alignItems: 'center',
              flexDirection: 'column',
            }}
          >
            {renderAvatar()}

            <Typography variant="subtitle1" noWrap sx={{ mt: 2 }}>
              @{user?.username}
            </Typography>

            <Typography variant="body2" sx={{ color: 'text.secondary', mt: 0.5 }} noWrap>
              {user?.email}
            </Typography>
          </Box>

          {renderList()}
        </Scrollbar>

        <Box sx={{ p: 2.5 }}>
          <LogOutButton onClose={onClose} />
        </Box>
      </Drawer>

      <UpdatePasswordDialog
        open={openPasswordDialog}
        onClose={() => setOpenPasswordDialog(false)}
      />

      <UpdateProfileDialog open={openProfileDialog} onClose={() => setOpenProfileDialog(false)} />
    </>
  );
}

export default AccountDrawer;
